#include <unistd.h>

void	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		//return (1);
		write(1, "yes", 3);
	else
		//return (0);
		write(1, "No", 2);
}

int	main()
{
	ft_isalpha(',');
	return (0);
}

